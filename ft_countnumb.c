#include "libft.h"

int	ft_countnumb(int n)
{
	int	cont;

	cont = 1;
	if (n < 0)
	{
		n = n * -1;
		cont++;
	}
	while (n >= 10)
	{
		n = n / 10;
		cont++;
	}
	return (cont);
}
